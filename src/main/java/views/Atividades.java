    public Atividades(Ong ong) {
        initComponents();

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("petcare");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();

        this.ongLogada = em.find(Ong.class, ong.getId());

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (Atividade atividade : this.ongLogada.getAtividades()) {
            model.addRow(new Object[]{atividade.getId(), atividade.getNome_atividade(), atividade.getLocal()});
        }

        em.close();
        emf.close();
    }

