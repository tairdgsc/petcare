    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.rowAtPoint(evt.getPoint());
        if (row >= 0) {
            Object rowData = jTable1.getValueAt(row, 0);
            Atividade atividadeAtual = null;

            for (Atividade atividade : this.ongLogada.getAtividades()) {
                if (Integer.toString(atividade.getId()).equals(rowData.toString())) {
                    atividadeAtual = atividade;
                }
            }

            JPanel parentPanel = (JPanel) this.getParent();

            parentPanel.removeAll();
            // Add new content to the mainPanel
            JPanel cadastrarPet = new CriarAtividade(this.ongLogada, atividadeAtual);
            parentPanel.add(cadastrarPet, BorderLayout.CENTER);

            // Repaint and revalidate the mainPanel to reflect the changes
            parentPanel.repaint();
            parentPanel.revalidate();
        }
    }//GEN-LAST:event_jTable1MouseClicked


